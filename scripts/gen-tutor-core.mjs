#!/usr/bin/env node
/**
 * Copy the single-source tutor logic (lambda/tutor/tutor-core.mjs) into a
 * TypeScript file the web app can consume: web/src/lib/tutor-core.generated.ts.
 *
 * Why a copy instead of importing the .mjs directly? ts-jest's transform key is
 * `^.+\.tsx?$`, so it does NOT transform .mjs; a cross-boundary .mjs re-export
 * makes the Jest suite fail with `SyntaxError: Unexpected token 'export'`. A .ts
 * copy is transformed to CommonJS (Jest loads it), and the `// @ts-nocheck`
 * banner suppresses `noImplicitAny` so `next build` (strict) type-checks clean.
 *
 * The generated file is gitignored and regenerated by the web `pretest`/`prebuild`
 * hooks. Paths resolve from this script's location, so cwd does not matter.
 */
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const ROOT = path.resolve(path.dirname(fileURLToPath(import.meta.url)), "..");
const SRC = path.join(ROOT, "lambda", "tutor", "tutor-core.mjs");
const OUT = path.join(ROOT, "web", "src", "lib", "tutor-core.generated.ts");

const banner =
  "// @ts-nocheck\n" +
  "// AUTO-GENERATED from lambda/tutor/tutor-core.mjs by scripts/gen-tutor-core.mjs.\n" +
  "// Do not edit; gitignored. Regenerated by the web pretest/prebuild hooks.\n";

writeFileSync(OUT, banner + readFileSync(SRC, "utf8"));
console.log(`Generated ${path.relative(ROOT, OUT)} from ${path.relative(ROOT, SRC)}`);
